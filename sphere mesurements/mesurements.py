radius = float(input ("What is the radius of the sphere "))

diameter = radius * 2
circumference = radius * 2 * 3.14159265359
surfaceArea = 4 * (radius * radius) * 3.14159265359
volume = (4/3) * 3.14159265359 * (radius * radius * radius)

print("the diameter is ", diameter)
print("the circumference is ", circumference)
print("the surface area is ", surfaceArea)
print("the volume is ", volume)